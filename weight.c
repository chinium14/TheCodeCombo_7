#include "defines.h"

/* ************************************************************************ */
/*                                                                          */
/* ======================== Begin Subroutine ============================== */
/*                                                                          */
/* ************************************************************************ */
void weight (void)
{
 
  for(int k=0; k<sim.NB; k++) {
    box[k].weight = 0.0;
    for(int i=0; i<box[k].boxns; i++) {
      box[k].weight += pott[k][i].mas;
    }
  }

}
